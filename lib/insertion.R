insert <- function(error){
  word <- c()
  for (i in 1:nchar(error)){
    word[i] <- paste(strsplit(error, "")[[1]][-i], collapse = "")
    
  }
  words <- data_frame(word)
  colnames(words) <- 'words'
  return(words)
}
